    public VentanaPrincipalGerente(UsuarioControlador uc,
            ReservaControlador rc, PaqueteTuristicoControlador ptc) {
        initComponents();
        i = new Inicio(ptc, uc, rc);
        e = new Empleados(uc);
        mv = new MetasVentas(rc);
        r = new ReporteCancelaciones(rc);
        rf = new ReporteFinanciero(rc);
        t = new Tarifas(ptc);
        mu = new MargenUtilidad(rc);
        pnlMenu.setPreferredSize(new Dimension(50, pnlMenu.getHeight()));
        pnlMenu.revalidate();
        JPanel[] paneles = {i,e,mv,r,rf,t,mu};

        for(int j = 0; j < paneles.length; j++){
            JPanel panel = paneles[j];
            lypPrincipal.add(panel);
        }

        mostrarPanel(i);
        seleccionarMenu(pnlInicio);
    }
    
